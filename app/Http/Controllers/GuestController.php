        $guest->nama = $request->nama;
        $guest->kelas = $request->kelas;
        $guest->keperluan = $request->keperluan;
        $guest->saran = $request->saran;
        $guest->save();
        return redirect('/elibrary')->with('success', 'Data Berhasil disimpan. Terima kasih atas partisipasi Anda');
    }

